// *************** comparison and logical operators ********************

let price = 100
let user = "mikehrmantraut"

// == if equal
console.log(price == 1) // false
console.log(price == "100") // true

// if value and data type is equal
console.log(price === 100) // true

// != if not equal
console.log(price != 50)

// if less than
console.log(price < 50)

// if less than equals
console.log(price <= 50)

// if more than
console.log(price > 40)

// if more than equals
console.log(price >= 40)

// && and
console.log(price > 0 && price < 100)

// || or
console.log(price > 0 || user != "mikehrmantraut")

